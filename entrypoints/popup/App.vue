  },
  {
    key: 'consentStatus',
    title: 'Consent Status',
    description: 'Display consent parameters in server GTM preview based on GA4/Data Tag payloads',
  },
]

const activeSettingsCount = computed(() => {
  return Object.values(settings).filter(Boolean).length
})

const toggleSetting = (key) => {
  settings[key] = !settings[key]
}

const showTooltip = (key) => {
  activeTooltip.value = key
}

const hideTooltip = () => {
  activeTooltip.value = null
}

const toggleTooltip = (key) => {
  if (activeTooltip.value === key) {
    activeTooltip.value = null
  } else {
    activeTooltip.value = key
  }
}

const openFeatureRequest = () => {
  window.open('https://feature.stape.io', '_blank')
}
</script>

