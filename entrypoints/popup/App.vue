  jsonFormatter: true,
  requestUrlFormatter: false,
  tagTypeColor: false,
  tagStatusColor: false,
  consentStatus: false,
})

const activeTooltip = ref(null)

const settingsConfig = [
  {
    key: 'jsonFormatter',
    title: 'JSON Formatter',
    description: 'Show beautified JSON of incoming/outgoing requests in server GTM preview',
